n = 2

A = list(input('Введите координаты Х и У для первой точки через пробел ').split()[:n])
B = list(input('Введите координаты Х и У для второй точки через пробел ').split()[:n])
a = int(A[0])
c = int(A[1])
x = int(B[0])
y = int(B[1])

k = (y - c)/(x - a)
b = y - k*x

print(f'Функция y = kx + b имеет вид y = {k}x + {b}')
